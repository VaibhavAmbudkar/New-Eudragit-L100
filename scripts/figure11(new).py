distinct_colors = ["#8E44AD", "#2980B9", "#16A085", "#27AE60",
                   "#F1C40F", "#E67E22", "#E74C3C", "#7F8C8D"]

def smooth(y, win=7):
    # centered moving-average for a clean, uncluttered curve
    if win < 2: return y
    kernel = np.ones(win)/win
    pad = win//2
    ypad = np.pad(y, pad, mode='edge')
    return np.convolve(ypad, kernel, mode='valid')[:len(y)]

for i, n in enumerate(nchains):
    d = rdf_data[n]
    r, g = np.array(d['r']), np.array(d['g'])
    g_s = smooth(g, win=7)
    fill_alpha = 0.28 if n < 12 else 0.40
    line_alpha = 0.65 if n < 12 else 0.95
    lw = 1.2 if n < 12 else 1.8
    zorder = 3 if n < 12 else 6
    ax.fill_between(r, 0, g_s, color=distinct_colors[i], alpha=fill_alpha, zorder=zorder, linewidth=0)
    ax.plot(r, g_s, color=distinct_colors[i], lw=lw, alpha=line_alpha, label=f'{n} chains', zorder=zorder+1)

ax.axhline(1, color='gray', lw=0.9, ls=':', zorder=1)
